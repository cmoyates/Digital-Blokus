	public int[][] getPiece(String pieceName) 
	{
		int tempNum = 0;
		for (int i = 0; i < 21; i++) 
		{
			if (pieceNameStrings[i].equals(pieceName)) 
			{
				tempNum = i;
			}
		}
		return pieceInfo[tempNum];
	}
	
	
	public int[][] getCornerInfo(String pieceName) 
	{
		int tempNum = 0;
		for (int i = 0; i < 21; i++) 
		{
			if (pieceNameStrings[i].equals(pieceName)) 
			{
				tempNum = i;
			}
		}
		return cornerInfo[tempNum];
	}
